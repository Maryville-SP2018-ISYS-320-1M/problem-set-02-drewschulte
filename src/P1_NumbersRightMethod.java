public class P1_NumbersRightMethod {

	public static void main(String[] args) {
		for (int i = 1; i <= 7; i++) {
			for (int j = 1; j <= (7 - i); j++) {
			System.out.print(" ");
			}
			for(int y = 1; y <= i; y++) {
			System.out.print(i);
	}
			System.out.println();
	}
	}
}